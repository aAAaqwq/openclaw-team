def _generate_xiang_judgment(base_hex: str, changed_hex: str, 
                                moving: List[int], question: str) -> str:
    """野鹤式象断——基于本变卦和动爻的直观判断"""
    parts = []
    base_img = GUA_DAXIANG.get(base_hex, "")
    changed_img = GUA_DAXIANG.get(changed_hex, "")
    
    parts.append(f"起得{base_hex}卦，{base_img[:20]}...")
    if moving:
        parts.append(f"动在{', '.join([['初','二','三','四','五','上'][m]+'爻' for m in moving])}, 化{changed_hex}。")
    else:
        parts.append("六爻安静。")
    
    # 野鹤关于"象"的判断：
    # 不拿理论绕人，直接说象
    if "泰" in base_hex:
        parts.append("时来天地皆同力。")
    elif "否" in base_hex:
        parts.append("运去英雄不自由。")
    elif "谦" in base_hex:
        parts.append("地中有山，内谦外卑，全卦最吉。")
    elif "剥" in base_hex:
        parts.append("硕果不食，君子尚消息盈虚。阳消阴长，宜守勿攻。")
    elif "复" in base_hex:
        parts.append("一阳来复，七日来复。阴极阳生，否极泰来。")
    elif "坎" in base_hex:
        parts.append("习坎，重险也。流水不舍昼夜，有诚信终出险。")
    elif "离" in base_hex:
        parts.append("明两作，大人以继明照于四方。光明在前。")
    elif "革" in base_hex or "鼎" in base_hex:
        parts.append("革故鼎新之象。旧的不去新的不来。")
    
    # 针对问题类型补充象断
    if question in ["财运", "交易"]:
        if "吉" in str(parts):
            parts.append("求财之占，此为顺象。")
        elif "大壮" in base_hex or "大有" in base_hex:
            parts.append("财富之象已显。")
    elif question in ["事业", "求职"]:
        if "晋" in base_hex or "升" in base_hex:
            parts.append("如日方升，事业可进。")
    elif question in ["婚姻"]:
        if "咸" in base_hex:
            parts.append("咸者感也，少男少女感应之象，最为婚吉。")
        elif "恒" in base_hex:
            parts.append("夫妇之道，不可以不久也。")
        elif "睽" in base_hex:
            parts.append("二女同居，其志不同行。不合之象。")
    
    return "".join(parts)


