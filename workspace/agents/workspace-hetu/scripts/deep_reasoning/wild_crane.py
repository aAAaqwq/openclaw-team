def simulate_yao(position: int) -> Tuple[int, bool]:
    """
    模拟铜钱起爻
    简化版：三枚铜钱，一个"正"面（阴=0）两个"反"面（阳=1）
    
    老方法三变：
    1变：分二+挂一+揲四+归奇
    简化：50%概率得老阴阳/少阴阳
    
    真实结果：6→老阴(动), 7→少阳, 8→少阴, 9→老阳(动)
    """
    r = random.randint(6, 9)
    return r, r in (6, 9)


def generate_hexagram(seed: Optional[int] = None) -> Dict:
    """
    生成一个六爻卦（本卦+变卦）
    六爻从下往上：初爻→二爻→三爻→四爻→五爻→上爻
    
    Returns:
        卦象信息
    """
    if seed is not None:
        random.seed(seed)
    
    lines = []
    moving_lines = []
    for i in range(6):
        val, is_moving = simulate_yao(i)
        lines.append(val)
        if is_moving:
            moving_lines.append(i)
    
    # 本卦二进制（从下到上，阴=0, 阳=1）
    base_binary = ''.join(['0' if v % 2 == 0 else '1' for v in lines])
    inner_tri = base_binary[:3]  # 下卦
    outer_tri = base_binary[3:]  # 上卦
    
    # 变卦（动爻阴阳翻转）
    changed = list(base_binary)
    for i in moving_lines:
        changed[i] = '1' if changed[i] == '0' else '0'
    changed_binary = ''.join(changed)
    
    # 识别卦名
    inner_name = triagram_to_name(inner_tri)
    outer_name = triagram_to_name(outer_tri)
    base_hex_name = hexagram_name(inner_tri, outer_tri)
    
    changed_inner = triagram_to_name(changed_binary[:3])
    changed_outer = triagram_to_name(changed_binary[3:])
    changed_hex_name = hexagram_name(changed_binary[:3], changed_binary[3:])
    
    return {
        "本卦": {
            "卦名": base_hex_name,
            "上卦": (outer_name, outer_tri),
            "下卦": (inner_name, inner_tri),
            "二进制": base_binary,
            "爻值": lines,
        },
        "变卦": {
            "卦名": changed_hex_name,
            "上卦": (changed_outer, changed_binary[3:]),
            "下卦": (changed_inner, changed_binary[:3]),
            "二进制": changed_binary,
            "爻值": [int(c) for c in changed_binary],
        },
        "动爻": [f"{['初','二','三','四','五','上'][i]}爻" for i in moving_lines],
        "动爻索引": moving_lines,
    }


