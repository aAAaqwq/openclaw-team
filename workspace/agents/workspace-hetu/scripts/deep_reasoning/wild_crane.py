    # 判吉凶
    if len(moving) == 0:
        # 静卦看用神旺衰
        verdict = "吉" if moon_wang >= 2 else ("凶" if moon_wang < 1 else "平")
    else:
        # 野鹤："独发之爻，力量最大"
        if len(moving) == 1:
            # 单一动爻力量巨大
            verdict = "吉" if moon_wang >= 2 else "凶"
        elif len(moving) == 2:
            verdict = "吉" if moon_wang >= 2 else "平"
        elif len(moving) >= 4:
            verdict = "凶"  # 乱动则凶
        else:
            verdict = "平"
    
    # 世应分析（简化）
